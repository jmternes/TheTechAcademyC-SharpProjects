using System;
using System.Collections.Generic;
using System.Text;

namespace MethodsAssignment
{
    public class Math
    {
        public int plusOne(int a, int b)
        {
            int sum = a + b;
            return sum;
        }

        public int divideByTwo(int a, int c)
        {
            int quotient = a / c;
            return quotient;
        }

        public int doubleIt(int a, int d)
        {
            int doubled = a * d;
            return doubled;
        }
    }
        
}
